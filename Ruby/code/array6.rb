# & と | 演算子
a = [1, 1, 2, 2]
b = [2, 2, 3, 3]
p a & b # => [2]
p a | b # => [1, 2, 3]

# + と - 演算子
a = [1, 1, 2, 2]
b = [2, 2, 3, 3]
p a + b # => [1, 1, 2, 2, 2, 2, 3, 3]
p a - b # => [1, 1]

c = [1]
p a - c # => [2, 2]

# *演算子
p [1, 2, 3] * 3       # => [1, 2, 3, 1, 2, 3, 1, 2, 3]
p [1, 2, 3] * '-'     # => "1-2-3"
p [1, 2, 3].join('-') # => "1-2-3"
